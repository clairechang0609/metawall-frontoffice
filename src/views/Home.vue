<template>
	<div class="home-wrap d-flex flex-column">
		<div class="row">
			<div class="col-12 col-md-7">
				<!-- 搜尋欄 -->
				<div class="row mb-3">
					<div class="col-4">
						<select name="type" class="form-control form-select rounded-0" v-model="search.sort" @change="getPosts()">
							<option value="desc">最新貼文</option>
							<option value="asc">最舊貼文</option>
						</select>
					</div>
					<div class="col-8">
						<div class="input-group">
							<input type="text" class="form-control rounded-0" placeholder="搜尋貼文" v-model="search.keyword" @keyup.enter="getPosts()">
							<button class="btn btn-primary shadow-none rounded-0 px-3 py-0 fs-5" type="button" @click="getPosts()">
								<i class="bi bi-search"></i>
							</button>
						</div>
					</div>
				</div>
				<!-- 貼文 -->
				<PostCard v-for="post in posts" :key="post.id" :is-empty="posts.length === 0" :is-loading="isLoading"
					:post="post" @update-post="getPosts()"></PostCard>
			</div>
			<div class="col-12 col-md-5 d-none d-md-block">
				<Sidebar></Sidebar>
			</div>
			<div class="d-md-none">
				<SidebarSm></SidebarSm>
			</div>
		</div>
	</div>
</template>

<script>
import { mapState } from 'vuex';
const ws = new WebSocket('wss://peaceful-citadel-43202.herokuapp.com/websockets');

export default {
	name: 'Home',
	components: {
		Sidebar: () => import('../components/Sidebar.vue'),
		SidebarSm: () => import('../components/SidebarSm.vue'),
		PostCard: () => import('../components/PostCard.vue')
	},
	data() {
		return {
			posts: [],
			search: {
				sort: 'desc', // 排序(預設降冪)
				keyword: '' // 關鍵字
			},
			isLoading: false
		};
	},
	computed: {
		...mapState({
			token: state => state.token
		})
	},
	mounted() {
		ws.onopen = () => console.log('WebSocket 服務已連接');
		ws.onclose = () => console.log('WebSocket 伺服器關閉');
		ws.onmessage = message => {
			if (typeof message.data === 'object') {
				this.getPosts();
			}
		};

		this.getPosts();
	},
	methods: {
		getPosts() {
			this.isLoading = true;
			const config = {
				method: 'GET',
				url: `${process.env.VUE_APP_APIPATH}/api/v1/posts`,
				headers: {
					authorization: `Bearer ${this.token}`
				},
				params: this.search
			};
			this.$http(config)
				.then(response => {
					this.posts = response.data.data;
					this.isLoading = false;
				})
				.catch(error => {
					console.log(error);
					this.isLoading = false;
				});
		}
	}
};
</script>

